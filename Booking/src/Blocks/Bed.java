package Blocks;

public class Bed {

	private int bedid;
	private int roomid;
	private int bedno;
	private String status;
	public int getBedid() {
		return bedid;
	}
	public void setBedid(int bedid) {
		this.bedid = bedid;
	}
	public int getRoomid() {
		return roomid;
	}
	public void setRoomid(int roomid) {
		this.roomid = roomid;
	}
	public int getBedno() {
		return bedno;
	}
	public void setBedno(int bedno) {
		this.bedno = bedno;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
	
}
